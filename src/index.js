// ‚úÖ Middleware para verificar la API Key en endpoints protegidos
const verificarAPIKey = (req, res, next) => {
    const apiKey = req.headers["x-api-key"];
    if (!apiKey || apiKey !== API_KEY) {
        return res.status(403).json({ success: false, message: "Acceso denegado. API Key incorrecta." });
    }
    next();
};

// ‚úÖ Middleware para loggear solicitudes
app.use((req, res, next) => {
    console.log(`üì¢ [${new Date().toISOString()}] ${req.method} ${req.url}`);
    next();
});

// ‚úÖ Webhook de verificaci√≥n
app.get("/webhook", (req, res) => {
    const mode = req.query["hub.mode"];
    const token = req.query["hub.verify_token"];
    const challenge = req.query["hub.challenge"];

    if (mode === "subscribe" && token === VERIFY_TOKEN) {
        return res.status(200).send(challenge);
    } else {
        return res.sendStatus(403);
    }
});


// ‚úÖ Endpoint para obtener consultas almacenadas con paginaci√≥n y seguridad
app.get("/consultas", verificarAPIKey, async (req, res) => {
    try {
        const { page = 1, limit = 10 } = req.query;
        const consultas = await Consulta.find()
            .sort({ fecha: -1 })
            .limit(parseInt(limit))
            .skip((parseInt(page) - 1) * parseInt(limit));
        res.json({ success: true, data: consultas });
    } catch (error) {
        console.error("‚ùå Error al obtener consultas:", error);
        res.status(500).json({ success: false, message: "Error al obtener las consultas" });
    }
});

// ‚úÖ Endpoint para eliminar consultas antiguas (mayores a X d√≠as)
app.delete("/consultas/limpiar", verificarAPIKey, async (req, res) => {
    try {
        const { dias = 30 } = req.query;
        const limiteFecha = new Date();
        limiteFecha.setDate(limiteFecha.getDate() - dias);
        await Consulta.deleteMany({ fecha: { $lt: limiteFecha } });
        res.json({ success: true, message: `Consultas de m√°s de ${dias} d√≠as eliminadas.` });
    } catch (error) {
        console.error("‚ùå Error al limpiar consultas:", error);
        res.status(500).json({ success: false, message: "Error al limpiar consultas" });
    }
});


